# Function to convert temperature from Celsius to Fahrenheit
def celsius_to_fahrenheit(celsius):
    # The formula to convert Celsius to Fahrenheit: (Celsius * 9/5) + 32
    fahrenheit = (celsius * 9/5) + 32
    return fahrenheit

